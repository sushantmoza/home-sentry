// GetVendor returns the probable vendor name for a given MAC address
func GetVendor(mac string) string {
	if len(mac) < 8 {
		return "Unknown"
	}
	// Normalize to xx:xx:xx (lowercase)
	prefix := strings.ReplaceAll(strings.ToLower(mac[:8]), "-", ":")

	if vendor, ok := macVendors[prefix]; ok {
		return vendor
	}
	return "Unknown"
}
